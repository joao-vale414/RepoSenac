SELECT
C.NOME AS CLIENTE,
P.ID AS PEDIDO_ID,
PR.PRODUTO AS PRODUTO
FROM 
CLIENTES C
LEFT JOIN PEDIDOS P ON P.CLIENTE_ID = C.ID
LEFT JOIN PRODUTOS PR ON P.PRODUTO_ID = PR.ID;



